public class O17_OneOrZero {
    public static void main(String[] args) {
        for (int a = 1; a <=20 ; a++)
        {
            if(a%2==0)
            {
                System.out.print(0 + " ");
            }
            else
            {
                System.out.print(1 + " ");
            }
        }
    }
}
