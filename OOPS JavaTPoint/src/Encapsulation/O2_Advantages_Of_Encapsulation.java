package Encapsulation;

public class O2_Advantages_Of_Encapsulation {
    /*
    By providing only a setter or getter method,
     you can make the class read-only or write-only.
      In other words, you can skip the getter or setter methods.

      It provides you the control over the data.  like we dont want to show the user the details and also hide it

      Suppose you want to set the value of id which should be greater than 100 only,
       you can write the logic inside the setter method.
        You can write the logic not to store the negative numbers in the setter methods.

        It is a way to achieve data hiding in Java because
         other class will not be able to access the data through the private data members.

         The encapsulate class is easy to test. So, it is better for unit testing.

     */
}
