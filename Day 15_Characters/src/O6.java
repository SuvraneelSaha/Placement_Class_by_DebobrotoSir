public class O6 {

    // printing all the characters ranging from 0 to 127

    public static void main(String[] args) {

        for (int i = 0; i <= 127 ; i++) {
            //System.out.print(i + " ");
            // int to character and character to integer conversion
            System.out.println(i + " " +(char)i);
        }
    }
}
